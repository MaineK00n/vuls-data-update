func (d *Source) Sort() {
	slices.Sort(d.Raws)
}

func Compare(x, y Source) int {
	return cmp.Or(
		cmp.Compare(x.ID, y.ID),
		slices.Compare(x.Raws, y.Raws),
	)
}
